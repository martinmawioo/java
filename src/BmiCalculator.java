/*
The  body mass index is the body mass in kilograms divided by the square of the
body height in meters. it is expressed as kg/m2
*/
import java.util.Scanner;
public class BmiCalculator {
    public static void main(String args[]){
        Scanner sc = new Scanner(System.in);
        System.out.println( "Enter weight in kilograms:\n");
        double weight= sc.nextInt();
        System.out.println("Enter height in meters:\n");
        double height= sc.nextInt();
        double BMI=weight/(height*height);
        System.out.println("\n The Body Mass Index is" +BMI+"kg/m2");
    }
}
